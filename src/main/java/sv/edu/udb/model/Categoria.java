package sv.edu.udb.model;
public class Categoria {
	 private int id;
	 private String nombre;
	 /**
	 * @return the id
	 */
	 public int getId() {
	 return id;
	 }
	 /**
	 * @param id the id to set
	 */
	 public void setId(int id) {
	 this.id = id;
	 }
	 /**
	 * @return the name
	 */
	 public String getName() {
	 return nombre;
	 }
	 /**
	 * @param name the name to set
	 */
	 public void setName(String nombre) {
	 this.nombre = nombre;
	 }
}